package homework;

public class Doors {
	
	public static void main(String[] args) {
		refrain();
		blankLine();
		
		System.out.println("I looked at you");
		System.out.println("You looked me");
		System.out.println("I smiled at you");
		System.out.println("You smiled at me");
		
		blankLine();
		
		System.out.println("And we're on our way");
		System.out.println("No we can't turn back, babe");
		System.out.println("Yeah, we're on our way");
		System.out.println("And we can't turn back");
		
		blankLine();
		refrain();
		blankLine();
		
		System.out.println("And we're on our way");
		System.out.println("No we can't turn back, babe");
		System.out.println("Yeah, we're on our way");
		
		blankLine();
		
		System.out.println("And we can't turn back, yeah");
		
		blankLine();
		
		System.out.println("I walked with you");
		System.out.println("You walked with me");
		System.out.println("I talked to you");
		System.out.println("You talked to me");
		
		blankLine();
		
		System.out.println("And we're on our way");
		System.out.println("No we can't turn back, yeah");
		System.out.println("Yeah, we're on our way");
		System.out.println("And we can't turn back, yeah");
		
		blankLine();
		refrain();
		blankLine();
		
		System.out.println("And we're on our way");
		System.out.println("No we can't turn back");
		System.out.println("Yeah, we're on our way");
		System.out.println("And we can't turn back");
		
		blankLine();
		refrain();
		blankLine();
	}
	public static void refrain() {	
		System.out.println("Cause it's too late");
		System.out.println("Too late, too late");
		System.out.println("Too late, too late");
	}
	public static void blankLine() {
		System.out.println("");
	}
}